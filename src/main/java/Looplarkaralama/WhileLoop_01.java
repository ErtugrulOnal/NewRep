package Looplarkaralama;

public class WhileLoop_01 {
    public static void main(String[] args) {

        //23 ten 12 ye kadar cift tamsayilari konsola yazdiriniz

        int i = 23;

        while (i > 11) {
            if (i % 2 == 0) {
                System.out.print(i + " ");

            }
            i--;

        }

    }
}
