public class Loop013 {
    public static void main(String[] args) {
        // 6 dan 3 e kadar tamsayilarin carpimini bulan kodu yaziniz

        int multiply=1;

        for (int i=6 ;  i>2   ; i--  ){

            multiply=multiply*i;

        }
        System.out.println(multiply);
    }

}
